const os = require('os');//Core Module - Operative System

//Platform
console.log(os.platform());

//CPU architecture
console.log(os.arch());

//CPU Core info
console.log(os.cpus());

//Mem Info
console.log(os.freemem());
console.log(os.totalmem());

//Home Dir
console.log(os.homedir());

//Uptime - Number of secons system has been Up
console.log(os.uptime());






